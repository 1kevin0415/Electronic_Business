package com.dd.electronicbusiness.controller;

import com.dd.electronicbusiness.model.Customer;
import com.dd.electronicbusiness.service.CustomerService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.*;

import java.util.List;

@RestController
@RequestMapping("/api/customers")
@CrossOrigin
public class CustomerController {

    @Autowired
    private CustomerService customerService;

    // API: 获取所有客户列表
    // 访问方式: GET http://localhost:8080/api/customers
    @GetMapping
    public List<Customer> getAllCustomers() {
        return customerService.getAllCustomers();
    }

    // API: 根据ID获取单个客户信息
    // 访问方式: GET http://localhost:8080/api/customers/1
    @GetMapping("/{id}")
    public Customer getCustomerById(@PathVariable Long id) {
        return customerService.getCustomerById(id);
    }

    // API: 创建新客户
    // 访问方式: POST http://localhost:8080/api/customers
    @PostMapping
    public Customer createCustomer(@RequestBody Customer customer) {
        return customerService.createCustomer(customer);
    }
}













































































































































































































































































































































